package javasef1;

public class Exer27 {
	public static void main(String[] args) {
		int i = 20;

		switch (i) {

		case 10: {
			System.out.println("é 10");
			System.out.println("primeiro case" + "\n");
		}

		case 20: {
			System.out.println("é 20");
			System.out.println("segundo case" + "\n");
		}

		case 30: {
			System.out.println("é 30");
			System.out.println("terceiro case" + "\n");
		}

		default: {
			System.out.println("default" + " -> não é nenhum valor esperado" + "\n");

		}

			System.out.println("-> o programa continua" + "\n");
		}
	}

}
