public class practice12 {
	public static void main(String[] args) {
		int var1 = 1;
		
		do {
			System.out.println("executando o bloco 1 ->" + " " + var1 + "\n");
			var1++;
			
		} while (var1 <=10);
		System.out.println("-> finalizado... " + "\n" + "-> executando o bloco 2");
	}

}
